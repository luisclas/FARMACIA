    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:

        try {

            if (txtRuc.getText().length() > 0) {
                if (txtRazonSocial.getText().length() > 0) {
                    if (txtRepresentante.getText().length() > 0) {
                        if (txtDireccion.getText().length() > 0) {
                            if (txtCorreo.getText().length() > 0) {
                                if (txtPaginaWeb.getText().length() > 0) {
                                    if (txtCelular.getText().length() > 0) {

                                        Proveedor oProveedor = new Proveedor();
                                        ProveedorBD oProveedorBD = new ProveedorBD();

                                        oProveedor.setProvRuc(txtRuc.getText().toUpperCase().trim());
                                        oProveedor.setProvRazonSocial(txtRazonSocial.getText().toUpperCase().trim());
                                        oProveedor.setProRepresentante(txtRepresentante.getText().toUpperCase().trim());
                                        oProveedor.setProvDireccion(txtDireccion.getText().toUpperCase().trim());
                                        oProveedor.setProvCorreo(txtCorreo.getText().toString());
                                        oProveedor.setProvWeb(txtPaginaWeb.getText().trim());
                                        oProveedor.setProvCelular(txtCelular.getText().trim());

                                        boolean rpta = oProveedorBD.registrarProveedor(oProveedor);
                                        if (rpta) {
                                            exito("Todo OK...");
                                            reportar_proveedor();
                                            limpiar();

                                        } else {
                                            error("Tiene problemas al registrar proveedor...");
                                        }

                                    } else {
                                        advertencia("Ingrese celular...");
                                        txtCelular.requestFocus();

                                    }

                                } else {
                                    advertencia("Ingrese pagina web...");
                                    txtPaginaWeb.requestFocus();
                                }

                            } else {
                                advertencia("Ingrese correo...");
                                txtCorreo.requestFocus();
                            }

                        } else {
                            advertencia("Ingrese direccion...");
                            txtDireccion.requestFocus();

                        }

                    } else {
                        advertencia("Ingrese representante...");
                        txtRepresentante.requestFocus();
                    }

                } else {
                    advertencia("Ingrese razon social...");
                    txtRazonSocial.requestFocus();
                }

            } else {
                advertencia("Ingrese ruc...");
                txtRuc.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (txtRuc.getText().length() > 0) {

            String ruc = txtRuc.getText();

            ProveedorBD oProveedorBD = new ProveedorBD();
            DefaultTableModel tabla_temporal;

            tabla_temporal = oProveedorBD.buscarProveedor(ruc);

            if (tabla_temporal.getRowCount() > 0) {

                tabla_reporte_proveedor.setModel(tabla_temporal);
                txtRuc.setText(tabla_temporal.getValueAt(0, 0).toString());
                txtRazonSocial.setText(tabla_temporal.getValueAt(0, 1).toString());
                txtRepresentante.setText(tabla_temporal.getValueAt(0, 2).toString());
                txtDireccion.setText(tabla_temporal.getValueAt(0, 3).toString());
                txtCorreo.setText(tabla_temporal.getValueAt(0, 4).toString());
                txtPaginaWeb.setText(tabla_temporal.getValueAt(0, 5).toString());
                txtCelular.setText(tabla_temporal.getValueAt(0, 6).toString());

                btnRegistrar.setEnabled(false);
                btnModificar.setEnabled(true);
                btnEliminar.setEnabled(true);

            } else {
                error("No se encontro datos...");
               
            }
        } else {
            advertencia("Ingrese ruc...");
            txtRuc.requestFocus();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

